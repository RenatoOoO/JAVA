package br.com.Ex_03;

public class Pessoa {

	private String nome;
	private String numero;
	
	public Pessoa(String nome, String numero) {
		super();
		this.nome = nome;
		this.numero = numero;
	}

	public Pessoa() {
		
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getNumero() {
		return numero;
	}

	public void setNumero(String numero) {
		this.numero = numero;
	}
	
	
	
	
	
}
